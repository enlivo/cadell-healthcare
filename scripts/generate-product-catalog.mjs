import { readdir, writeFile } from 'node:fs/promises';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const root = path.resolve(path.dirname(fileURLToPath(import.meta.url)), '..');

const categories = [
  { slug: 'cardiac', label: 'Cardiac Care', folder: 'cardiac' },
  { slug: 'diabetic', label: 'Diabetic Care', folder: 'diabetic' },
  { slug: 'general-medicine', label: 'General Medicine', folder: 'general-medicine' },
  { slug: 'gynaecology', label: 'Gynaecology', folder: 'gynaecology' },
  { slug: 'orthopedic', label: 'Orthopedic', folder: 'orthopedic' },
];

const imageExtensions = new Set(['.jpg', '.jpeg', '.png', '.webp', '.avif']);

const catalog = {};

for (const category of categories) {
  const directory = path.join(root, 'public', 'images', 'products', category.folder);
  const files = (await readdir(directory))
    .filter((file) => imageExtensions.has(path.extname(file).toLowerCase()))
    .sort((a, b) => a.localeCompare(b, undefined, { numeric: true, sensitivity: 'base' }));

  catalog[category.slug] = {
    label: category.label,
    folder: category.folder,
    products: files.map((file) => ({
      name: file.replace(/\.[^.]+$/, ''),
      image: `/images/products/${category.folder}/${file}`,
    })),
  };
}

const output = `// Auto-generated by scripts/generate-product-catalog.mjs. Do not edit manually.
export const productCatalog = ${JSON.stringify(catalog, null, 2)};
`;

await writeFile(path.join(root, 'src', 'data', 'productCatalog.generated.js'), output);
